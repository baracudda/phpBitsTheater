	/**
	 * Top Level Domain by Country Code (ISO 3166-1 alpha-2)
	 * @see https://en.wikipedia.org/wiki/ISO_3166-1_alpha-2
	 * @var string
	 */
	public function cc_to_tld($a2charCountryCode) {
		//strtoupper seems a waste since we should never be using lowercase country codes
		//$a2charCountryCode = strtoupper($a2charCountryCode);
		switch ($a2charCountryCode) {
			case 'GB':
				return '.uk';  //see: https://en.wikipedia.org/wiki/.gb
			default:
				return '.'.strtolower($a2charCountryCode);
		}//switch
	}

